private void jButtonExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExportActionPerformed
    try {
        this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        if (this.jCheckBoxBedGraph.isSelected()) {
            DataManager.exportBEDGraph(data, this.jTextFieldHeader.getText().toString(),
                    this.jTextFieldFileName.getText().toString());
        } else {
            DataManager.exportBED(data, this.jTextFieldHeader.getText().toString(),
                    this.jTextFieldFileName.getText().toString());
        }

        JOptionPane.showMessageDialog(this, "exported into "
                + this.jTextFieldFileName.getText());
        this.dispose();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "error  - see logfile for more information: "
                + ex.getMessage());
    } finally {
        this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }

    }//GEN-LAST:event_jButtonExportActionPerformed

